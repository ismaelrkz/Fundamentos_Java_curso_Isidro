public class testeIncremento{

    public static void main(String args[]){

        int a = 3;

        System.out.println("Valor de a = " + a++);
        System.out.println("Valor de a = " + a);

        System.out.println("Valor de a = " + ++a);
        System.out.println("Valor de a = " + a);

    }

}